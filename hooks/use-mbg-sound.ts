import { useCallback } from 'react'

type SoundType = 'navOpen' | 'buttonPress' | 'toggleChange' | 'modalOpen' | 'modalClose' | 'formSubmit' | 'success' | 'error'

const SOUND_DATA: Record<SoundType, string> = {
  navOpen: 'data:audio/wav;base64,UklGRiYAAABXQVZFZm10IBAAAAABAAEAQB8AAAB9AAACABAAZGF0YQIAAAAAAA==',
  buttonPress: 'data:audio/wav;base64,UklGRiYAAABXQVZFZm10IBAAAAABAAEAQB8AAAB9AAACABAAZGF0YQIAAAAAAA==',
  toggleChange: 'data:audio/wav;base64,UklGRiYAAABXQVZFZm10IBAAAAABAAEAQB8AAAB9AAACABAAZGF0YQIAAAAAAA==',
  modalOpen: 'data:audio/wav;base64,UklGRiYAAABXQVZFZm10IBAAAAABAAEAQB8AAAB9AAACABAAZGF0YQIAAAAAAA==',
  modalClose: 'data:audio/wav;base64,UklGRiYAAABXQVZFZm10IBAAAAABAAEAQB8AAAB9AAACABAAZGF0YQIAAAAAAA==',
  formSubmit: 'data:audio/wav;base64,UklGRiYAAABXQVZFZm10IBAAAAABAAEAQB8AAAB9AAACABAAZGF0YQIAAAAAAA==',
  success: 'data:audio/wav;base64,UklGRiYAAABXQVZFZm10IBAAAAABAAEAQB8AAAB9AAACABAAZGF0YQIAAAAAAA==',
  error: 'data:audio/wav;base64,UklGRiYAAABXQVZFZm10IBAAAAABAAEAQB8AAAB9AAACABAAZGF0YQIAAAAAAA==',
}

export function useMBGSound() {
  const playSound = useCallback((type: SoundType, volume: number = 0.15) => {
    try {
      const audio = new Audio(SOUND_DATA[type])
      audio.volume = volume
      audio.play().catch(() => {})
    } catch (error) {
      console.error('[MBG] Sound playback failed:', error)
    }
  }, [])

  return { playSound }
}
